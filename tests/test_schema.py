@pytest.mark.parametrize("missing_key", ["name", "system_prompt", "model", "scorer", "cases"])
def test_missing_required_field(tmp_path, missing_key):
    base = {
        "name": "t",
        "system_prompt": "p",
        "model": "m",
        "scorer": "exact",
        "cases": [{"user": "hi", "expected": "hello"}],
    }
    base.pop(missing_key)
    lines = "\n".join(f"{k}: {v!r}" for k, v in base.items() if not isinstance(v, list))
    if "cases" in base:
        lines += "\ncases:\n  - user: hi\n    expected: hello"
    f = tmp_path / "suite.yaml"
    f.write_text(lines)
    with pytest.raises(SuiteValidationError) as exc_info:
        load_suite(f)
    assert missing_key in str(exc_info.value)


