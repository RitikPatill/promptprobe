import textwrap
from pathlib import Path

import pytest

from promptprobe.schema import Case, Suite, SuiteValidationError, load_suite

FIXTURES = Path(__file__).parent / "fixtures"


def test_load_valid_suite():
    suite = load_suite(FIXTURES / "valid_suite.yaml")
    assert suite.name == "translation-quality"
    assert suite.scorer == "contains"
    assert len(suite.cases) == 2
    assert suite.cases[0].id == "case_0"
    assert suite.cases[1].id == "farewell-case"


